package chap02;

public class IntegerOperation {
    public static void main(String[] args) {
        // 2개의 정수를 합하여 그 결과를 a에 대입
        int a = 23 + 12;
        // a의 결과를 출력
        System.out.println("a = " + a);
        // a와 long 길이의 정수 1을 합하여 그 결과를 l에 대입
        long l = a + 1L;
        // l의 결과를 출력
        System.out.println("l = " + l);
    }
}
