package chap02;

public class StringVariable {
    public static void main(String[] args) {
        // 문자열 변수 선언 및 대입
        String stringval = "string 변수입니다.";
        // 문자열 변수 객체 선언 및 대입
        String stringobj = new String("string 객체입니다.");

        System.out.println("string 변수 = " + stringval);
        System.out.println("string 객체 = " + stringobj);
        System.out.println();
    }
}
