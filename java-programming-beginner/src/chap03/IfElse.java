package chap03;

public class IfElse {
    public static void main(String[] args) {
        System.out.println("-->단순 Else if문 실습<--\n");
        int x = 30;

        if (x < 20){
            System.out.println("이 문장은 If 문장입니다");
        }else{
            System.out.println("이 문장은 else 문장입니다");
        }
        System.out.println();
    }
}
