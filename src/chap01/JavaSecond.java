package chap01;

public class JavaSecond {
    public static void main(String[] args) {
        // 다음 줄로 넘어가지 않는 문자열 출력
        System.out.print("환영합니다 ");
        // 이어진 문자열 출력
        System.out.println("자바세계는 이름답습니다!");

        // 다음 줄에 문자열 출력
        System.out.println("다음은 자바에서 기본 수식입니다!");
        // 변수 선언
        int result;
        // 더하기 연산
        result = 2 +3;
        // 문자열과 함께 결과 출력
        System.out.println(" 2 + 3 = " + result);
    }
}
